def es_par(numero):
    if numero % 2 == 0:
        return "El número es par"
    else:
        return "El número es impar"

# Prueba y cobertura de sentencia
print(es_par(4))

# Prueba y cobertura de decisión
print(es_par(3))

# Prueba y cobertura de condición
print(es_par(0))

# Prueba y cobertura de camino
print(es_par(4))
print(es_par(3))
print(es_par(0))



